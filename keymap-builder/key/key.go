type Mod string
type Key string

func (k Key) Less(other Key) int {
	return cmp.Compare(k, other)
}

func modBuilder(name string) func(Key) Key {
	return func(k Key) Key {
		return Key(fmt.Sprintf("%s(%s)", name, k))
	}
}

func KeyFrom(b byte) Key {
	if b >= 'A' && b <= 'Z' {
		return Key(b)
	}

	switch b {
	// case '\a':
	// case '\f':
	// case '\r':
	// case '\v':
	case ' ':
		return SPACE
	case '\b':
		return LEFT
	case '\n':
		return RETURN
	case '\t':
		return TAB
	}

	panic(fmt.Sprintf("unhandled key: '%c'", b))
}

